CREATE TABLE t_system_user
(
    ID              BIGINT          NOT NULL,
    USER_NAME       VARCHAR(100) NOT NULL COMMENT '用户名',
    NICK_NAME       VARCHAR(100) NOT NULL COMMENT '昵称',
    PHONE_NUMBER    INT          NOT NULL COMMENT '手机号',
    PASS_WORD       VARCHAR(255) NULL DEFAULT NULL COMMENT '登录密码',
    LAST_LOGIN_TIME VARCHAR(20) NULL DEFAULT NULL COMMENT '最后登录时间',
    CREAT_TIME      VARCHAR(20) NULL DEFAULT NULL COMMENT '创建时间',
    UPDATE_TIME     VARCHAR(20) NULL DEFAULT NULL COMMENT '更新时间',
    DEL_FLAG        BIT(1)       NOT NULL DEFAULT 0 COMMENT '删除标志:1-是，0-否',
    PRIMARY KEY (ID)
) COMMENT='系统用户表';

